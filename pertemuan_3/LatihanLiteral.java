public class LatihanLiteral {
    public static void main(String[] args) {
        System.out.println("=== DEMONSTRASI LITERAL TIPE DATA ===");

        //tipe data byte
        byte maksimum = 127;
        System.out.println("Tipe byte           : " + maksimum);

        //tipe data short
        short tahun = 2026;
        System.out.println("Tipe short          : " + tahun);

        //tipe data int
        int populasiind = 275000000;
        System.out.println("Tipe int            : " + populasiind);
        
        //tipe data long
        long populasidunia = 8000000000L;
        System.out.println("Tipe long           : " + populasidunia);

        //tipe data float 
        float phi = 3.14f;
        System.out.println("Tipe float          : " + phi);

        //tipe data double
        double nilaiE = 2.718281828;
        System.out.println("Tipe double         : " + nilaiE);

        //tipe data char
        char hurufpertama = 'B';
        System.out.println("Tipe char           : " + hurufpertama);

        //tipe data boolean;
        boolean status = true;
        System.out.println("Tipe boolean        : " + status);
    }
}